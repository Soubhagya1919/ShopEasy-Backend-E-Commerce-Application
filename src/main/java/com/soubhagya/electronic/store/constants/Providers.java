package com.soubhagya.electronic.store.constants;

/**
 * The Providers enum represents different authentication providers
 * that a user can use to log into the application.
 *
 * The supported providers are:
 * - GOOGLE: Represents authentication via Google.
 * - FACEBOOK: Represents authentication via Facebook.
 * - GITHUB: Represents authentication via GitHub.
 * - SELF: Represents authentication through the application's own system.
 */
public enum Providers {
    GOOGLE, FACEBOOK, GITHUB, SELF
}
